/*==============================================================================
  NVIC 初期設定処理関数
  void cm4_nvic_init( void )
  
 -------------------------------------------------------------------------------
  << Usage >>
  [ IN ]
  なし
  
  [ OUT ]
  なし
==============================================================================*/
void cm4_nvic_init( void )
{
	NVIC_ICPR0 = 0xFFFFFFFFUL ;
	NVIC_ISER0 = ( 1UL << 28U ) ;

	NVIC_ISER1 = ( 1UL << 5U ) ;

	return ;
}

