/*==============================================================================
    デバッグ用LED点灯処理関数
  void debug_led_on( void )
  
 -------------------------------------------------------------------------------
  << Usage >>
  [ IN ]
     なし
  
  [ OUT ]
     なし
==============================================================================*/
void debug_led_on( void )
{
	/* GPIOA5 -> HIGH */
	stm32_gpio_out( PORT_A, 5, U8_LVL_HIGH ) ;

	return ;
}

/*==============================================================================
    デバッグ用LED消灯処理関数
  void debug_led_off( void )

 -------------------------------------------------------------------------------
  << Usage >>
  [ IN ]
     なし

  [ OUT ]
     なし
==============================================================================*/
void debug_led_off( void )
{
	/* GPIOA5 -> LOW */
	stm32_gpio_out( PORT_A, 5, U8_LVL_LOW ) ;

	return ;
}
